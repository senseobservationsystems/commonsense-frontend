package nl.sense_os.commonsense.server.data;

public class Sensor {

	private String id;
	private String name;

	public Sensor() {
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

}
